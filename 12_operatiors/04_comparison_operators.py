# Comparison operators example
# Comparison operators er maddhome amra duita value ke compare korte pari
# result hobe True or False

"""
| Operator | Meaning          |
| -------- | ---------------- |
| `==`     | equal            |
| `!=`     | not equal        |
| `>`      | greater          |
| `<`      | less             |
| `>=`     | greater or equal |
| `<=`     | less or equal    |

"""


# comparison operator example

a = 10
b = 10

print(a == b)   # a and b equal kina check kore, output hobe True
print(a != b)   # a and b not equal kina check kore, output hobe False
print(a > b)    # a b theke boro kina check kore, output hobe False
print(a < b)    # a b theke choto kina check kore, output hobe False
print(a >= b)   # a b theke boro or equal kina check kore, output hobe True
print(a <= b)   # a b theke choto or equal kina check kore, output hobe True

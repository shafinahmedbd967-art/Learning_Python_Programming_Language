# explicit type conversion example
# programmer nijer theke datatype convert kore

num = "50"     # string value

number = int(num)    # num ke integer e convert kora hocche, karon num er value holo "50" jeita string, ar amra chay num er value ke number e convert korte

print(number)       # number variable er value print kora hocche
print(type(number))  # number variable er type print kora hocche


"""
output:
50
<class 'int'>
"""
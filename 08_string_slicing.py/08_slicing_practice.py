# slicing practice

country = "Bangladesh"

print(country[0:4])  # index 0 theke 3 porjonto
print(country[2:7])  # index 2 theke 6 porjonto
print(country[-5:-1])  # index -5 theke -2 porjonto (reverse order e access)
print(country[::-1])   # step -1 diye string ke reverse kora hoy


marks = int(input("Enter your marks: "))
 
# ek ek kore condition check hobe upor theke niche
if marks >= 90:
    print("Grade: A+")    
 
elif marks >= 80:
    print("Grade: A")     
 
elif marks >= 70:
    print("Grade: B")     
 
elif marks >= 60:
    print("Grade: C")     
 
else:
    print("Grade: F")    
 
'''
output: Grade: B
'''